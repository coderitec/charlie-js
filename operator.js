let num1 = 5
let num2 = 10
let num3 = 25

//addition 
let sum = num1 + num2 
console.log("The sum of num1 and num2 is:", sum)

//subtraction
let difference = num3 - num1
console.log("The difference between num3 and num1 is:", difference)

//multiplication
let product = num1 * num2 * num3
console.log("The product of num1, num2, and num3 is:", product)

//division
let quotient = sum / num1
console.log("The quotient of num3 divided by num1 is:", quotient)

// modulus
let remainder = 100 % 101
console.log("The remainder of 100 divided by 120 is:", remainder)

// exponentiation
let power = num2 ** 3
console.log("The result of num2 raised to the power of 3 is:", power)

// addition assignment
sum += num3 
console.log("The new sum after adding num3 is:", sum)

// subtraction assignment
// difference = difference - 7
difference -= 7
console.log("The new difference after subtracting 7 is:", difference)

// multiplication assignment
product *= 2.7
console.log("The new product after multiplying by 2.7 is:", product)

// division assignment
quotient /= 10
console.log("The new quotient after dividing by 10 is:", quotient)

// modulus assignment
remainder %= 3
console.log("The new remainder after modulus 3 is:", remainder)

// exponentiation assignment
power **= 1/3
console.log("The new power after taking the cube root is:", power)

// increment
num1++
console.log("The incremented value of num1 is:", num1)

// decrement
num2--  
console.log("The decremented value of num2 is:", num2)

//create a variable x and assign it the increment of sum
let x = ++sum
console.log("The value of x after incrementing sum is:", x)
console.log("The new value of sum is:", sum)

// create a variable y and assign it the decrement of difference
let y = --difference
console.log("The value of y after decrementing difference is:", y)



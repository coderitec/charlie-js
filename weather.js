const weather = 'crazy'

switch (weather) {
    case 'normal':
        alert('Enjoy the weather, everywhere is clear!');
        break;
    case 'rainy':
        alert('Take an umbrella!');
        break;
    case 'sunny':
        alert('Wear sunglasses and hat!');
        break;
    case 'snowy':
        alert('Wear a warm coat and boots!');
        break;
    case 'windy':
        alert('Hold onto your hat!');
        break;
    default:
        alert('Weather is unpredictable, be prepared for anything!');
        break;

}